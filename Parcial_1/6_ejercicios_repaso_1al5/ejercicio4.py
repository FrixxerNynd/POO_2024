#Solicitar 2 numeros al usuario y realizar todas las operaciones basicas de una calculadora (+,-,*,/) y mostrar por pantalla el resultado


num1 = int(input("Ingresa un numero: \n"))
num2 = int(input("Ingresa un numero: \n"))


resultado = num1 + num2
print(f"La operacion basica de la suma de {num1} + {num2} es: ", resultado)

resultado = num1 * num2
print(f"La operacion basica de la suma de {num1} * {num2} es: ", resultado)

resultado = num1 / num2
print(f"La operacion basica de la suma de {num1} / {num2} es: ", resultado)

resultado = num1 - num2
print(f"La operacion basica de la suma de {num1} - {num2} es: ", resultado)